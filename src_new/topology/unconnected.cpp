#include "base.h"
#include "unconnected.h"

namespace pagmo { namespace topology {

/// Default constructor.
unconnected::unconnected():base() {}

/// Clone method.
base_ptr unconnected::clone() const
{
	return base_ptr(new unconnected(*this));
}

/// Push back implementation.
/**
 * Will call base::add_node() on the input index.
 *
 * @param[in] idx positional index of the island to be inserted.
 */
void unconnected::push_back(int idx)
{
	add_node(idx);
}

}}
